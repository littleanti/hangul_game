/**
 * 한자 학습 단계 추정 — 5단계 학습자가 어느 한자까지 익혔는지 판단할 때 사용
 * 8급 / 7급 → grade 5에 노출, 6급 / 5급 → grade 6에 노출, 그 이상 → 보스 한정.
 */
export function getHanjaByGrade(grade) {
  const map = { 5: ["8급", "7급"], 6: ["8급", "7급", "6급", "5급"] };
  const allowed = new Set(map[grade] || []);
  return Object.entries(HANJA)
    .filter(([, info]) => allowed.has(info.grade))
    .map(([char, info]) => ({ char, ...info }));
}

/**
 * 의미 카테고리 인접 한자 — 디스트랙터 가중치에 사용
 */
export function getHanjaByCategory(category, excludeSet = new Set()) {
  return Object.entries(HANJA)
    .filter(([char, info]) => info.category === category && !excludeSet.has(char))
    .map(([char, info]) => ({ char, ...info }));
}

/**
 * 한자 정보 조회 (없으면 placeholder 반환 — 게임 깨짐 방지)
 */
export function lookupHanja(char) {
  return HANJA[char] || { sound: "?", meaning: "?", strokes: 0, grade: "?", category: "기타" };
}
